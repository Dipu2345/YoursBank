    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {    
            HttpSession sson = request.getSession();
            String to = (String)sson.getAttribute("changeUserEmail");
            String from ="debashispandadipu@gmail.com";
            ActivityDao ad = new ActivityDao(DbConnect.getConnect());
            String message ="Your UserName Is :"+ ad.getUserNameByEmail(to)+" :   Thank You....";
            String subject ="Yours Bank ";
            if(sendEmail.sendEmailToUser(message, subject, from, to))
                    out.print("success");
            else
                out.print("error");
          
        }
    }

